        // Fungsi untuk menginisialisasi observer
        function initCounterObserver() {
            const counters = document.querySelectorAll('.counter');
            const options = {
                threshold: 0.8 // Jika 80% dari elemen muncul dalam viewport
            };

            // Membuat instance dari Intersection Observer
            const observer = new IntersectionObserver(function(entries) {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        startCounter(entry.target);
                        observer.unobserve(entry.target); // Berhenti mengamati setelah mulai counter
                    }
                });
            }, options);

            // Mengamati setiap elemen counter
            counters.forEach(counter => {
                observer.observe(counter);
            });
        }

        // Fungsi untuk memulai counter
        function startCounter(counterElement) {
            const targetCount = parseInt(counterElement.innerText);
            let currentCount = 0;
            const increment = Math.ceil(targetCount / 100); // Meningkatkan increment sesuai dengan jumlah target

            const counterInterval = setInterval(() => {
                currentCount += increment;
                if (currentCount >= targetCount) {
                    currentCount = targetCount;
                    clearInterval(counterInterval); // Hentikan interval setelah mencapai target
                }
                counterElement.innerText = currentCount;
            }, 20); // Interval untuk setiap update counter (misalnya setiap 20ms)
        }

        // Panggil fungsi inisialisasi ketika dokumen siap
        document.addEventListener('DOMContentLoaded', function() {
            initCounterObserver();
        });
    </script>
